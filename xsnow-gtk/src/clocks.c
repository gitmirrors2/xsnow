#include <sys/times.h>
#include <sys/time.h>
#include <unistd.h>
#include "clocks.h"

double wallclock() 
{ 
	struct timeval toot;
	double r;

	gettimeofday(&toot,0);
	r=toot.tv_sec+0.000001*(double)toot.tv_usec;
	return(r);
}

